def area(a, b): 
    ''' Принимает стороны прямоугольника a, b и возвращает площадь прямоугольника.
	Пример вызова функции: 
	area(б, 7)
	>> 42 '''
    return a * b 

def perimeter(a, b):
    ''' Принимает стороны прямоугольника a, b и возвращает периметр прямоугольника.
	Пример вызова функции: 
	perimeter(б, 7)
	>> 26 '''
    return (a + b) * 2